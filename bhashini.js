window.translateTextToText = function(text, targetLang, callback) {
  console.log(`[Bhashini Translator] Simulating Text translation for: "${text}" into "${targetLang}"`);
  
  if (!text) {
    callback("");
    return;
  }

  // Artificial latency simulation (500ms) to feel like real AI translation call
  setTimeout(() => {
    let result = text;
    if (targetLang === 'en') {
      // Simulate high-fidelity English mapping for registry records
      if (text.includes("काटना") || text.includes("কামড়") || text.includes("கடி") || text.includes("కాటు")) {
        result = `[Bhashini AI Auto-Translation]: A bite case description was recorded: "${text}" 🩹`;
      } else {
        result = `${text} [Auto-Translated to English] 🌐`;
      }
    }
    callback(result);
  }, 500);
};
