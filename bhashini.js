// Bhashini Widget Creation & Core DOM Translation Logic
window.translateDOM = function(lang) {
  // Fallback to English dictionary if specific language dictionary is not defined
  const dict = window.BHASHINI_DICTIONARY[lang] || window.BHASHINI_DICTIONARY['en'];
  if (!dict) return;

  // Feature 5: Preferred Language Storage (PDF Page 3 & 12 Compliance)
  try {
    localStorage.setItem('bhashini_preferred_lang', lang);
  } catch (err) {
    console.warn("[Bhashini Plugin] LocalStorage access blocked:", err);
  }

  const elements = document.querySelectorAll('[data-i18n]');
  elements.forEach(el => {
    // Feature 3: Selective Translation - Skip translation for elements marked with bypass class (PDF Page 3 & 11 Compliance)
    if (el.classList.contains('bhashini-skip-translation') || el.closest('.bhashini-skip-translation')) {
      return;
    }

    const key = el.getAttribute('data-i18n');
    if (dict[key]) {
      let translation = dict[key];
      
      // Feature 2: Glossary Addition (PDF Page 3 & 12/15 Compliance)
      const glossary = window.BHASHINI_GLOSSARY && window.BHASHINI_GLOSSARY[lang];
      if (glossary) {
        // Direct key replacement or substring matching
        Object.keys(glossary).forEach(term => {
          if (translation.includes(term)) {
            translation = translation.replace(new RegExp(term, 'g'), glossary[term]);
          }
        });
      }
      
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = translation;
      } else {
        // Safely preserve standard child tags like FontAwesome icons
        const icon = el.querySelector('i');
        
        if (icon) {
          const iconClone = icon.cloneNode(true);
          el.innerHTML = '';
          el.appendChild(iconClone);
          el.appendChild(document.createTextNode(' ' + translation));
        } else {
          el.textContent = translation;
        }
      }
    }
  });
};

